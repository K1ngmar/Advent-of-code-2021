lines = [x for x in open("d10.input").read().split()]

matches = {'(':')', '[':']', '{':'}', '<':'>'}
rating = {')':3, ']':57, '}':1197, '>':25137}

score = 0
stack = []
for line in lines:
	for c in line:
		if c in matches:
			stack.append(c)
		elif matches[stack[len(stack) - 1]] != c:
			score += rating[c]
			break
		else:
			stack.pop()

print(score)

lines = open("d02.input").read().split()

pos = 0
depth = 0
aim = 0

for i in range(len(lines) - 1):
	try:
		amt = int(lines[i + 1])
	except:
		pass
	if lines[i] == "forward":
		pos += amt
		depth += amt * aim
	elif lines[i] == "up":
		aim -= amt
	elif lines[i] == "down":
		aim += amt

print(depth * pos)

lines = open("d03.input").read().split()
l2 = lines.copy()

for i in range(len(lines[0])):
	b1 = str(int(sum(map((lambda x:x[i]=="1"), lines)) >= len(lines) / 2))
	lines = [l for l in lines if l[i] == b1]
	if len(l2) > 1:
		b2 = str(int(sum(map((lambda x:x[i]=="1"), l2)) >= len(l2) / 2))
		l2 = [l for l in l2 if l[i] != b2]


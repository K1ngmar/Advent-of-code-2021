lines = open("d03.input").read().split()

gamma = ""
for i in range(len(lines[0])):
	if sum(map((lambda x:x[i]=="1"), lines)) > len(lines) / 2:
		gamma += "1"
	else:
		gamma += "0"
print(int(gamma, 2) * (int(gamma, 2) ^ 0xFFF))
